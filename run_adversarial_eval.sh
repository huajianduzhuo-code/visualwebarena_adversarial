        "agent_model": os.environ.get("AGENT_MODEL", ""),
        "attacker_model": os.environ.get("ATTACKER_MODEL", ""),
        "total_tasks": total,
        "attack_success_rate": len(attacked) / total,
        "defensive_stop_rate": len(defensive) / total,
        "defense_rate": (total - len(attacked)) / total,
        "task_pass_rate": len(passed) / total,
        "total_time_seconds": total_time,
        "site_stats": site_stats,
        "per_task_results": all_results,
    }
    merged_path = os.path.join(result_dir, "adversarial_merged.json")
    with open(merged_path, "w") as f:
        json.dump(merged, f, indent=2, default=str)
    print(f"\nMerged results saved to {merged_path}")
else:
