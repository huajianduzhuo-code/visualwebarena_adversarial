    print(f"  Attack Success Rate: {len(attacked)/total*100:.1f}%  ({len(attacked)}/{total})")
    print(f"  Defensive Stop Rate: {len(defensive)/total*100:.1f}%  ({len(defensive)}/{total})")
    print(f"  Defense Rate:        {(total-len(attacked))/total*100:.1f}%  ({total-len(attacked)}/{total})")
    print(f"  Task Pass Rate:      {len(passed)/total*100:.1f}%  ({len(passed)}/{total})")
    total_time = int(os.environ.get("TOTAL_TIME", "0"))
    if total_time > 0:
        print(f"  Total time:          {total_time//60}m {total_time%60}s ({total_time/total:.1f}s/task)")
    print(f"{'='*60}")

    if attacked:
        print(f"\nAttacked tasks:")
        field_counts = Counter()
        for r in attacked:
            fields = r.get("leaked_fields", [])
            field_counts.update(fields)
            print(f"  task_id={r['task_id']}, step={r['attack_step']}, leaked={fields}")
